// Everything in Java is an object (which are instantiated based on a class)
// This class represents our program
class HelloWorld {
    // Objects have data (attributes) and behavior (methods)
    // In this case this is a program, so it needs a 'Main' behavior
    // All methods return something, in this case VOID which means nothing
    public static void main(String[] args) {
        // JDK has built-in libraries to help us getting started
        System.out.println("Hello Summer Semester!");
    }
}